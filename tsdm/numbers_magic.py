def addition(arg1, arg2):
    """
    This functions adds the first and the second argument.

    :param arg1: the first summand
    :type arg1:  float
    :param arg2: the second and last summand
    :type arg2:  float
    :return:     the sum of both summands
    :rtype:      float
    """
    return arg1 + arg2
