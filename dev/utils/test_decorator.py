def multiplying(f_py=None, factor=1):
    assert callable(f_py) or f_py is None

    def _decorator(func):
        @functools.wraps(func)
        def wrapper(*args, **kwargs):
            return factor * func(*args, **kwargs)

        return wrapper

    return _decorator(f_py) if callable(f_py) else _decorator


