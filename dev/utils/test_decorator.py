def debug(func):
    @wraps(func)
    def wrapper(*func_args, **func_kwargs):
        print("\n" + "-" * 80 + "\n")
        print(f"Inside {func}")
        print(f"{signature(func)=}")
        print(f"{func_args=}")
        print(f"{func_kwargs=}")
        return func(*func_args, **func_kwargs)

    return wrapper


